age = input ('Age: ')
if age <(str(2)):
    print ('It is a baby')
elif age >(str(1)) and age <(str(4)):
    print ('It is a toddler')
elif age >(str(3)) and age <(str(13)):
    print ('It is a kid')
elif age >(str(12)) and age <(str(20)):
    print ('It is a teenager')
elif age >(str(19)) and age <(str(65)):
    print ('It is a adult')
elif age >(str(64)):
    print ('It is a elder')
